    public void showOnTable(List<Base> list){
        //获取制定（tblProduct）表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        //2清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //显示数据
        for(Base p : list){
            //颜色警戒
            Vector vt = new Vector();
            vt.add(p.getBid());
            vt.add(p.getBname());
            vt.add(p.getBaddr());
            dtm.addRow(vt);
        }
    }

