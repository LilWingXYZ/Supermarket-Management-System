    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
        //1获取商品信息
        int bid = Integer.parseInt(this.txtBid.getText().trim());
        String bname = this.txtBname.getText().trim();
        String baddr = this.txtBaddr.getText().trim();
        //数据格式验证使用StringUtil
        //1验证非空
        try{
            if(bid==0){
                JOptionPane.showMessageDialog(this, "仓库编号为空");
            }
            if(StringUtil.checkLength(bname)==false){
                JOptionPane.showMessageDialog(this, "仓库名称为空");
            }
            if(StringUtil.checkLength(baddr)==false){
                JOptionPane.showMessageDialog(this, "仓库地址为空");
            }
        }catch(Exception e){
            return;
        }
        DBLink lk = new DBLink();
        Connection conn = lk.getConnection();
        Base p = new Base();
        p.setBid(bid);
        p.setBaddr(baddr);
        p.setBname(bname);
        boolean result = false;
        try {
            result = pbiz.add(conn,p);
        } catch (SQLException ex) {
            Logger.getLogger(BaseADCS.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(BaseADCS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(result == true){
            JOptionPane.showMessageDialog(this, "添加成功");
            List<Base>list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空面板信息
        clearInput();
    }                                      

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          
        int answer = JOptionPane.showConfirmDialog(this, "您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            int proid = Integer.parseInt(this.txtBid.getText());
            boolean result = false;
            DBLink lk = new DBLink();
            Connection conn = lk.getConnection();
            try {
                result = pbiz.delete(conn ,proid);
            } catch (SQLException ex) {
                Logger.getLogger(BaseADCS.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(BaseADCS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(result){
                JOptionPane.showMessageDialog(this, "删除成功");
                List<Base>list = pbiz.findAll();
                //显示list中的信息
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this, "删除失败");
            }
        }
    }                                         

